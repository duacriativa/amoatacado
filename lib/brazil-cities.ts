export function normalizeCityKey(cidade: string, uf: string): string {
    return cidade
        .trim()
        .toLowerCase()
        .normalize('NFD')
        .replace(/[̀-ͯ]/g, '')
        .replace(/[^a-z\s]/g, '')
        .trim()
        .replace(/\s+/g, ' ') + '_' + uf.toLowerCase().trim();
}

export function getCityCoords(cidade: string, uf: string): [number, number] | null {
    if (!cidade || !uf) return STATE_CAPITALS[uf?.toLowerCase()] || null;
    const key = normalizeCityKey(cidade, uf);
    return CITY_COORDS[key] || STATE_CAPITALS[uf.toLowerCase()] || null;
}
